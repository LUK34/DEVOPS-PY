# logical OR operator
print("---------------------------------------------")
print("With BOOLEAN Values: ")
print("True or True -> ",True or True)
print("True or False -> ",True or False)
print("False or True -> ",False or True)
print("False or False -> ",False or False)
print("---------------------------------------------")
print("With INTEGER Values: ")
print("0 or 0 -> ", 0 or 0)
print("0 or 1 -> ", 0 or 1)
print("1 or 0 -> ", 1 or 0)
print("1 or 1 -> ", 1 or 1)
print("-1 or -10 -> ", -1 or -10)
print("---------------------------------------------")
print("With FLOAT Values: ")
print("1.23 or 0.00 -> ", 1.23 or 0.00)
print("0.0001 or 0.0101010 -> ", 0.0001 or 0.0101010)
print("0.0000000001 or 0.0000000101010 -> ", 0.0000000001 or 0.0000000101010)
print("---------------------------------------------")
print("With COMPLEX numbers: ")
print("1+0j or 0-1j -> ", 1+0j or 0-1j) # 1 or 0 -> 1
print("0j or 1-1j -> ", 0j or 1-1j) # 0 or 1 -> 1
print("---------------------------------------------")
print("With STRING: ")
print('a' or 'b')
print('  ' or 'r')
print('' or 'p')
