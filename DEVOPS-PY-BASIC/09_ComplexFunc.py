print(complex(11,22)) # 11 -> real and 22 -> imaginary
print(complex(-101,-202)) # -101 -> real and -202 -> imaginary
print(complex(3021, 0b11001)) # 3021 -> real and (0b11001 -> (binary -> decimal -> complex) -> imaginary)
print(complex(0XAf12, 0o11001)) # (0XAf12 -> (Hexadecimal -> decimal -> real)) and (0o11001 -> (Octal -> decimal -> imaginary))