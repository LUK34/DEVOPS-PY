def addition(a,b):
    c=a+b
    return c
def subtraction(a,b):
    c=a-b
    return c
def multiplication(a,b):
    c=a*b
    return c
def division(a,b):
    c=a/b
    return c
print("-------------------------------------------------\n")
a=int(input("Enter the first number:"))
b=int(input("Enter the second number:"))
print("-------------------------------------------------\n")
result1=addition(a,b)
result2=subtraction(a,b)
result3=multiplication(a,b)
result4=division(a,b)
print("Addition Result: ",result1,"\n")
print("Subtraction Result: ",result2,"\n")
print("Multiplication Result: ",result3,"\n")
print("Division Result: ",result4,"\n")
print("-------------------------------------------------\n")


